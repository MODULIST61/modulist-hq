export function Sidebar({ collapsed, onToggle, mobileOpen, onMobileClose }) {
  const { currentUser } = useAuth()
  const { notifications } = useData()
  const items = getVisibleNavItems(currentUser, NAV_ITEMS)
  const unreadMentions = notifications.filter(
    (n) => n.user_id === currentUser?.id && !n.okundu && n.tip === 'mention'
  ).length

  const content = (
    <aside
      className={cn(
        'flex flex-col h-full bg-primary text-white transition-all duration-200',
        collapsed ? 'w-[72px]' : 'w-[260px]'
      )}
    >
      <div className={cn('flex items-center gap-3 px-4 py-5 border-b border-white/10', collapsed && 'justify-center px-2')}>
        <img src="/modulist-logo.png" alt="Modulist" className="h-7 brightness-0 invert shrink-0" />
        {!collapsed && (
          <div>
            <div className="font-semibold text-sm leading-tight">Modulist HQ</div>
            <div className="text-[10px] text-white/60">hq.modulist.net</div>
          </div>
        )}
      </div>

      <nav className="flex-1 py-3 px-2 overflow-y-auto">
        {Object.entries(
          items.reduce((acc, item) => {
            const sec = item.section || 'genel'
            if (!acc[sec]) acc[sec] = []
            acc[sec].push(item)
            return acc
          }, {})
        ).map(([section, sectionItems]) => (
          <div key={section} className="mb-2">
            {!collapsed && NAV_SECTIONS[section] && (
              <div className="px-3 py-2 text-[10px] font-semibold uppercase tracking-wider text-white/40">
                {NAV_SECTIONS[section]}
              </div>
            )}
            <div className="space-y-0.5">
              {sectionItems.map((item) => (
                <NavLink
                  key={item.path}
                  to={item.path}
                  end={item.path === '/'}
                  onClick={onMobileClose}
                  className={({ isActive }) =>
                    cn(
                      'flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-all duration-150',
                      isActive ? 'bg-white/15 text-white shadow-sm' : 'text-white/70 hover:bg-white/10 hover:text-white',
                      collapsed && 'justify-center px-2'
                    )
                  }
                >
                  {icons[item.icon]}
                  {!collapsed && (
                    <>
                      <span className="flex-1">{item.label}</span>
                      {item.path === '/mesajlar' && unreadMentions > 0 && (
                        <span className="bg-accent text-white text-xs px-1.5 py-0.5 rounded-full min-w-[20px] text-center">{unreadMentions}</span>
                      )}
                    </>
                  )}
                </NavLink>
              ))}
            </div>
          </div>
        ))}
      </nav>

      <button
        onClick={onToggle}
        className="hidden lg:flex items-center justify-center py-3 border-t border-white/10 text-white/50 hover:text-white text-xs"
      >
        {collapsed ? '→' : '← Daralt'}
      </button>
    </aside>
  )

  return (
    <>
      <div className="hidden lg:block shrink-0">{content}</div>
      {mobileOpen && (
        <div className="lg:hidden fixed inset-0 z-40 flex">
          <div className="absolute inset-0 bg-black/50" onClick={onMobileClose} />
          <div className="relative z-10">{content}</div>
        </div>
      )}
    </>
  )
}
